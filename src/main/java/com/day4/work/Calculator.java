package com.day4.work;

public class Calculator {
	//加法:算2个int的和
	public int add(int num1,int num2){
		return num1+num2;
	} 
	
//	减法:算2个int的差
	public int subtract(int num1,int num2){
		return num1-num2;
	} 
	
//	乘法:算3个double的乘积
	public double multiplicat(double num1,double num2,double num3){
		return num1*num2*num3;
	} 

//	除法:算2个double的商
	public double divis(double num1,double num2){
		return num1/num2;
	}
}
